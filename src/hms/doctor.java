    private void Psearch1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Psearch1ActionPerformed
        if(Puserid.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Please enter Patient id");
            return;
        }
        else
        {
            String userid=Puserid.getText().trim().toUpperCase();
            String Ause_reg=("^[A-Za-z]{3}[0-9]{3,4}$");
            if(Pattern.matches(Ause_reg,userid))
            {
                try
                {
                    String sql="SELECT p.name, p.age, p.disease , a.appointment_time, "+
                    " a.diagnosis , a.prescription FROM new_patient p "+
                    "LEFT JOIN appointment a on p.userid = a.userid where p.userid =?";
                    String url="jdbc:mysql://localhost:3306/hospitalmana";
                    Connection conn=DriverManager.getConnection(url,"root","root");
                    PreparedStatement ps=conn.prepareStatement(sql);
                    ps.setString(1,Puserid.getText());
                    ResultSet rs=ps.executeQuery();
                    if(rs.next())
                    {
                        // JOptionPane.showMessageDialog(null, "data found");
                        Pname.setText(rs.getString("name"));
                        Page.setText(rs.getString("age"));
                        Pdisease.setText(rs.getString("disease"));
                        Pappointmenttime.setText(rs.getString("appointment_time"));
                        Pdiagnosis.setText(rs.getString("diagnosis"));
                        Pprescription.setText(rs.getString("prescription"));
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(null,"Data not found");
                    }
                    rs.close();
                    ps.close();
                    conn.close();
                }
                catch(Exception ex)
                {
                    JOptionPane.showMessageDialog(null,""+ex);
                }
            }
        }
    }//GEN-LAST:event_Psearch1ActionPerformed

