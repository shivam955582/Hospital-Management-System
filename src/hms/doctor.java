    private void filterpatientbydate(java.util.Date selectedDate) //shivam
    {
        if (sorter == null) return;

        if (selectedDate == null) {
            sorter.setRowFilter(null); // show all
            return;
        }
        java.sql.Date sqlDate = new java.sql.Date(selectedDate.getTime());
        sorter.setRowFilter(new RowFilter<DefaultTableModel, Integer>() {
            @Override
            public boolean include(RowFilter.Entry<? extends DefaultTableModel, ? extends Integer> entry) {
                Object dateValue = entry.getValue(6); // appointment_date column
                if (dateValue == null) return false;
                return dateValue.toString().equals(sqlDate.toString());
            }
        });
    }

