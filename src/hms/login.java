    private void LsigninActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LsigninActionPerformed
        if(Lrole.getSelectedItem().toString().equals("Select Role")||
           Luserid.getText().trim().isEmpty()||
           Lpassword.getPassword().length==0)
        {
            JOptionPane.showMessageDialog(null,"Enter the all value");
        }
        else
        {
            try
            {   String role = Lrole.getSelectedItem().toString();
                String userid = Luserid.getText().trim();
                char p[]=Lpassword.getPassword();
                String pass=String.valueOf(p);
                String sql="SELECT *from staff_registration where role=? and userid=? and password=? and leavingdate is null";
                String url="jdbc:mysql://localhost:3306/hospitalmana";
                Connection conn =DriverManager.getConnection(url,"root","root");
                PreparedStatement ps=conn.prepareStatement(sql);
                ps.setString(1,role);
                ps.setString(2,userid);
                ps.setString(3,pass);
                ResultSet rs=ps.executeQuery();
                if(rs.next())
                {
                    JOptionPane.showMessageDialog(null, "Login Successfully");
                    this.dispose();
                    if(role.equals("Admin"))
                    {
                        new admin().setVisible(true);
                    }
                    else if(role.equals("Doctor"))
                    {
                        String doctorname=rs.getString("name");
                        new doctor(doctorname).setVisible(true);
                    }
                    else if(role.equals("Receptionist"))
                    {
                        new receptionist().setVisible(true);
                    }
                 rs.close();
                 ps.close();
                 conn.close();
                }
                else
                {
                    JOptionPane.showMessageDialog(null,"Please Enter Right Information");
                    Lrole.setSelectedIndex(0);
                    Luserid.setText(null);
                    Lpassword.setText(null);
                }    
            }
            catch(Exception ex)
            {
                JOptionPane.showMessageDialog(null," "+ex);
            }
        }
    }//GEN-LAST:event_LsigninActionPerformed

