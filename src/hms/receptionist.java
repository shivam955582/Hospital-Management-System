    private boolean slotavailable(String doctor,java.sql.Date date,String timeslot )
    {
        boolean available=true;
        try
        {
            String sql="SELECT COUNT(*)FROM appointment where doctor=? and appointment_date=? and appointment_time=? and status='BOOKED'";
            String url="jdbc:mysql://localhost:3306/hospitalmana";
            Connection conn=DriverManager.getConnection(url,"root","root");
            PreparedStatement ps=conn.prepareStatement(sql);
            ps.setString(1,doctor);
            ps.setDate(2,date);
            ps.setString(3,timeslot);
            ResultSet rs=ps.executeQuery();
            if(rs.next()&& rs.getInt(1)>0)
            {
                available=false;
            }
            rs.close();
            ps.close();
            conn.close();
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,""+ex);
        }
        return available;
    }
