    private String generateuserid(String role){
        String prefix="";
        switch(role){
            case "Doctor":prefix="DOC";
            break;
            case "Nurse":prefix="NUR";
            break;
            case "Receptionist":prefix="REC";
            break;
            case "Staff":prefix="STF";
            break;
            case "Wardboy":prefix="WAR";
            break;
            case "Guard":prefix="GAR";
            break;           
        }
        String newid=prefix+"001";
        try
        {
           String url="jdbc:mysql://localhost:3306/hospitalmana";
           String sql="SELECT userid FROM staff_registration WHERE userid LIKE? ORDER BY userid DESC LIMIT 1";
           Connection conn=DriverManager.getConnection(url,"root","root");
           PreparedStatement ps=conn.prepareStatement(sql);
           ps.setString(1,prefix+"%");
           ResultSet rs=ps.executeQuery();
           if(rs.next()){
               String lastid=rs.getString("userid");
               int num =Integer.parseInt(lastid.substring(3));
               newid=prefix+String.format("%03d",num+1);  
           }
           rs.close();
           ps.close();
           conn.close();
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,"");
        }
        return newid;
    }
