    private void FsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FsaveActionPerformed
      if(Fuserid.getText().isEmpty()||
         Femail.getText().isEmpty()||
         Fnpassword.getPassword().toString().length()==0||
         Fcpassword.getPassword().toString().length()==0
        )
      {
          JOptionPane.showMessageDialog(null,"Enter the all value");
      }
      else
      {
          try
          {
              char np[]=Fnpassword.getPassword();
              char cp[]=Fcpassword.getPassword();
              String npass=String.valueOf(np);
              String cpass =String.valueOf(cp);
              if(npass.equals(cpass)){
                  String sql="UPDATE staff_registration set password =? where userid=? and email=?";
                  String url="jdbc:mysql://localhost:3306/hospitalmana";
                  Connection conn=DriverManager.getConnection(url,"root","root");
                  PreparedStatement ps=conn.prepareStatement(sql);
                  ps.setString(1,npass);
                  ps.setString(2,Fuserid.getText());
                  ps.setString(3,Femail.getText());
                  int a=ps.executeUpdate();
                  if(a>0)
                  {
                      JOptionPane.showMessageDialog(null,"Password change successful");
                      login lp =new login();
                      lp.setVisible(true);
                      this.setVisible(false);
                  }
                  else
                  {
                      JOptionPane.showMessageDialog(null,"E-mail and Contact are not same");
                      Fuserid.setText(null);
                      Femail.setText(null);
                      Fnpassword.setText(null);
                      Fcpassword.setText(null);  
                  }
              }
              else
              {
                JOptionPane.showMessageDialog(null,"Password are not matched");
                Fnpassword.setText(null);
                Fcpassword.setText(null);
              }
          }
          catch(Exception ex)
          {
              JOptionPane.showMessageDialog(null,""+ex);
          }
      }
    }//GEN-LAST:event_FsaveActionPerformed

